function HeroTextScramble({ titles }: { titles: string[] }) {
    const [textIndex, setTextIndex] = useState(0);
    const currentTitle = titles[textIndex % titles.length];
    const nextIndex = (textIndex + 1) % titles.length;
    const nextTitle = titles[nextIndex];
    const [trigger, setTrigger] = useState(false);

    useEffect(() => {
        setTrigger(true);
        const intervalId = setInterval(() => {
            setTextIndex((prevIndex) => (prevIndex + 1) % titles.length);
            setTrigger(true);
        }, 3000); // Change text every 3 seconds

        return () => clearInterval(intervalId);
    }, []);

    useEffect(() => {
        if (textIndex !== 0) {
            setTrigger(true);
        }
    }, [textIndex]);

    return (
        <h1 className="mb-6 text-4xl font-bold tracking-tight sm:text-6xl md:text-8xl md:mb-8">
            <span className="text-transparent bg-clip-text bg-gradient-to-b from-white to-white/80">
                <TextScramble trigger={trigger} onScrambleComplete={() => setTrigger(false)}>
                    {currentTitle}
                </TextScramble>
            </span>
            <br />
            <span
                className={cn(
                    "bg-clip-text text-transparent bg-gradient-to-r from-indigo-300 via-white/90 to-rose-300 "
                )}
            >
                {nextTitle}
            </span>
        </h1>
    );
}



